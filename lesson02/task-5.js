'use strict';
/**
 * Сложение двух чисел
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function add (a, b) {
    return a + b;
}

/**
 * Вычитание двух чисел
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function sub (a, b) {
    return a - b;
}

/**
 * Умножение двух чисел
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function mult (a, b) {
    return a * b;
}

/**
 * Деление двух чисел
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function div (a, b) {
    return a / b;
}

console.log(add(10, 5));
console.log(sub(10, 5));
console.log(mult(10, 5));
console.log(div(10, 5));
